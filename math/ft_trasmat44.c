#include "../libft.h"

void	ft_trasmat44(t_mat44 tras, t_mat44 const mat)
{
	char	i;
	char	j;

	i = -1;
	while (++i < 4)
	{
		j = -1;
		while (++j < 4)
			tras[(int)i][(int)j] = mat[(int)j][(int)i];
	}
}
